public class int_to_byte_to_float {
    public static void main(String[] args){
        int a=3222;
        byte b= (byte)a;
        float c= b;

        System.out.println("int a = " + a);
        System.out.println("byte b = (byte)a = " + b);
        System.out.println("float c = b = " + c);
    }
    
}
